"""Audit all catalog datasets via the API service layer."""

from __future__ import annotations

import argparse
import sys
from dataclasses import dataclass
from datetime import UTC, datetime
from pathlib import Path

from dataset_visualizer.api.service import get_dataset_meta, get_overview, get_sample
from dataset_visualizer.config import load_config

OUTPUT_PATH = Path(__file__).resolve().parents[1] / "docs" / "audit" / "dataset-audit-matrix.md"


@dataclass
class AuditRow:
    """One dataset audit result."""

    dataset_id: str
    viewer: str
    meta_ok: bool
    overview_ok: bool
    sample_ok: bool
    viewer_matches_id: bool
    notes: str


def _audit_dataset(dataset_id: str) -> AuditRow:
    """Run API checks for one dataset id."""
    notes: list[str] = []
    meta_ok = overview_ok = sample_ok = viewer_matches_id = False
    viewer = "—"

    try:
        meta = get_dataset_meta(dataset_id)
        meta_ok = True
        viewer = str(meta.get("viewer") or "—")
        viewer_matches_id = viewer == dataset_id
        if not viewer_matches_id:
            notes.append(f"viewer={viewer}")
    except Exception as err:
        notes.append(f"meta: {err}")

    try:
        overview = get_overview(dataset_id, {}, {})
        overview_ok = bool(overview.get("metrics"))
        if not overview.get("charts") and not overview.get("tables"):
            notes.append("no charts/tables")
    except Exception as err:
        notes.append(f"overview: {err}")

    try:
        sample = get_sample(dataset_id, 0, {}, {})
        sample_ok = sample.get("row") is not None
        if sample.get("total", 0) == 0:
            notes.append("empty dataset")
    except Exception as err:
        notes.append(f"sample: {err}")

    return AuditRow(
        dataset_id=dataset_id,
        viewer=viewer,
        meta_ok=meta_ok,
        overview_ok=overview_ok,
        sample_ok=sample_ok,
        viewer_matches_id=viewer_matches_id,
        notes="; ".join(notes),
    )


def _all_dataset_ids() -> list[str]:
    config = load_config()
    ids: list[str] = []
    for datasets in config.categories.values():
        ids.extend(entry.id for entry in datasets)
    return sorted(ids)


def render_matrix(rows: list[AuditRow]) -> str:
    """Render audit rows as markdown."""
    passed = sum(
        1
        for row in rows
        if row.meta_ok and row.overview_ok and row.sample_ok and row.viewer_matches_id
    )
    timestamp = datetime.now(UTC).strftime("%Y-%m-%d %H:%M UTC")
    lines = [
        "# Dataset audit matrix",
        "",
        f"Generated by `scripts/audit_datasets.py` on {timestamp}.",
        "",
        f"**Summary:** {passed}/{len(rows)} datasets passed all checks.",
        "",
        "| Dataset | Viewer | Meta | Overview | Sample | Viewer=id | Notes |",
        "|---------|--------|------|----------|--------|-----------|-------|",
    ]
    for row in rows:

        def mark(ok: bool) -> str:
            return "pass" if ok else "fail"

        lines.append(
            f"| {row.dataset_id} | {row.viewer} | {mark(row.meta_ok)} | "
            f"{mark(row.overview_ok)} | {mark(row.sample_ok)} | "
            f"{mark(row.viewer_matches_id)} | {row.notes or '—'} |"
        )
    lines.append("")
    return "\n".join(lines)


def audit_datasets(*, output: Path = OUTPUT_PATH) -> list[AuditRow]:
    """Audit every catalog dataset and write the matrix markdown."""
    rows = [_audit_dataset(dataset_id) for dataset_id in _all_dataset_ids()]
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(render_matrix(rows), encoding="utf-8")
    return rows


def main() -> int:
    """CLI entry point."""
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--output",
        type=Path,
        default=OUTPUT_PATH,
        help="Markdown output path",
    )
    args = parser.parse_args()
    rows = audit_datasets(output=args.output)
    failed = [
        row.dataset_id
        for row in rows
        if not (row.meta_ok and row.overview_ok and row.sample_ok and row.viewer_matches_id)
    ]
    print(f"Wrote {args.output} ({len(rows)} datasets, {len(rows) - len(failed)} passed)")
    if failed:
        print("Failures:", ", ".join(failed), file=sys.stderr)
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
